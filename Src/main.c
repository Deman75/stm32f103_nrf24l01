void MotorDrive(uint8_t direction, uint16_t power) {
	
	if (direction == 1 && Volt > 6.6) {
		ENG2R_Stop;
		ENG1R_Stop;
		ENG1D_Start;
		ENG2D_Start;
		ENG2PWR = 800+(power*MaxPwr); // Задний двигатель
		ENG1PWR = 800+(power*MaxPwr);
		
//		LEDFL_ON;
//		LEDFR_ON;
	}
	
	if (direction == 2 && Volt > 6.6) {
		ENG2D_Stop;
		ENG1D_Stop;
		ENG1R_Start;
		ENG2R_Start;
		ENG2PWR = 800+(power*MaxPwr); // Задний двигатель
		ENG1PWR = 800+(power*MaxPwr);
		
//		LEDFL_OFF;
//		LEDFR_OFF;
	}	
	
	if (direction == 0 || Volt < 6.6) {
		ENG2D_Stop;
		ENG1D_Stop;
		ENG1R_Stop;
		ENG2R_Stop;
		ENG1PWR = 0;
		ENG2PWR = 0;
		
//		LEDFL_OFF;
//		LEDFR_OFF;
	}		
	
}

uint8_t CheckBits (uint8_t value, uint8_t pos){
	if (value & (1<<pos))
		return 1;
	else 
		return 0;
}


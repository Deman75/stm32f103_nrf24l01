/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	uint8_t usar=0x01;
	uint8_t dt_reg=0;
	uint8_t LightButton = 0;
	
	uint8_t test;

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_SPI1_Init();
  MX_USART1_UART_Init();
  MX_TIM1_Init();
  MX_ADC1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */
	
	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_3);
	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_4);
	HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);
	
	HAL_Delay(200);
	
	NRF24_ini();
	HAL_Delay(100);
	HAL_TIM_Base_Start_IT(&htim2);
	
	ENG1PWR=0; //200-1000
	ENG2PWR=0;
	PWM3=100;
	PWM4=SRV_C;
	
	RX_BUF[0] = 100;
	RX_BUF[1] = 100;
	
	HAL_ADC_Start_DMA(&hadc1,(uint32_t*) &ADC_Data,2);
	
	LEDFL_ON;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
	
		NRF24L01_Receive();
		HAL_Delay(1);
		
		Volt = 0.00276 * ADC_Data[0];
		memcpy(buf1, &Volt, sizeof(float));
		
	
		
		if (TimeLastRx > 500) {
			RX_BUF[0] = 100;
			RX_BUF[1] = 100;
			RX_BUF[2] = 0;
			RX_BUF[3] = 0;
			RX_BUF[4] = 0;
			TimeLastRx = 501;
		}
		
		if (OptionFlag == 0){ // Если нет запроса на настройку.
			if (RX_BUF[1] > 105) 
				MotorDrive(1, RX_BUF[1]-100);
			if (RX_BUF[1] < 95) 
				MotorDrive(2, 100-RX_BUF[1]);
			if (RX_BUF[1]<105 && RX_BUF[1]>95)
				MotorDrive(0,0);
				
			if (RX_BUF[0] > 105)
				PWM4 = SRV_C + (((SRV_Right-SRV_C)/100.0)*(RX_BUF[0]-100));
			if (RX_BUF[0] < 95)
				PWM4 = SRV_C - (((SRV_C-SRV_Left)/100.0)*(100-RX_BUF[0]));
			if (RX_BUF[0]<105 && RX_BUF[0]>95)
				PWM4 = SRV_C;

			if (CheckBits(RX_BUF[2],1)) {
				if (LightButton == 0) {
					Light_tog;
					LightButton = 1;
				}
			} else {
				LightButton = 0;
			}

			if (CheckBits(RX_BUF[2],2))
					LEDRL_ON;
				else
					LEDRL_OFF;
			if (CheckBits(RX_BUF[2],3))
					LEDRR_ON;
				else
					LEDRR_OFF;				
		}
  }
  /* USER CODE END 3 */

}

